#Make a function for addition and subtaction  
def add(x, y):
    '''This function adds 2 numbers'''
    return x + y 
def subtract(x, y):
    '''This function subtracts 2 numbers'''
    return x - y
# Example
print(f"Addition of 3 and 4: {add(3, 4)}")
print(f"subtraction of 5 and 2: {subtract(5, 2)}")
